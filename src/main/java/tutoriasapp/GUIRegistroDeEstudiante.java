    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if(!camposVacios()) {
            if(validarMatricula()) {
                if(guardarEstudiante()) {
                    this.setVisible(false);
                    JOptionPane.showMessageDialog(null, "El estudiante se ha registrado exitosamente.", "Estudiante registrado", 1);
                } else {
                    JOptionPane.showMessageDialog(null, "El estudiante no ha sido registrado.", "Estudiante no registrado", 0);
                }
            } else {
                lblCamposVacios.setText("Matricula inválida");
            }
        } else {
            lblCamposVacios.setText("Hay campos vacíos");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

