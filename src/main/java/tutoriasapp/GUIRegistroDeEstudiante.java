    private boolean camposVacios() {
        boolean camposVacios = true;
        if(!(tfNombre.getText().trim().isEmpty())
                && !(tfApellidoPaterno.getText().trim().isEmpty())
                && !(tfApellidoMaterno.getText().trim().isEmpty())
                && !(tfMatricula.getText().trim().isEmpty())
                && !(cbProgramaEducativo.getSelectedItem() == null)
                && !(cbTutorAcademico.getSelectedItem() == null)) {
            camposVacios = false;
        }
        return camposVacios;
    }

    public boolean validarMatricula() {
        String matricula = tfMatricula.getText();
        boolean matriculaCorrecta = false;
        if(matricula.length() == 9 && (matricula.charAt(0) == 83 || matricula.charAt(0) == 115)) {
            matriculaCorrecta = true;   
        }
        for(int i=1; i<matricula.length(); i++) {
            if(matricula.charAt(i) < 48 || matricula.charAt(i) > 57) {
                matriculaCorrecta = false;
            }
        }
        return matriculaCorrecta;
    }
    
    private boolean guardarEstudiante() {
        Persona personaEstudiante = new Persona(tfNombre.getText(),tfApellidoPaterno.getText(),tfApellidoMaterno.getText());
        TutorAcademico tutorAcademico = (TutorAcademico)cbTutorAcademico.getSelectedItem();
        ProgramaEducativo programaEducativo = (ProgramaEducativo)cbProgramaEducativo.getSelectedItem();
        Estudiante estudiante = new Estudiante(tfMatricula.getText(), personaEstudiante, tutorAcademico.getIdTutorAcademico(), programaEducativo.getIdProgramaEducativo());
        EstudianteDAO estudianteDAO = new EstudianteDAO();
        return estudianteDAO.addEstudiante(estudiante);
    }
    
    private void llenarCBProgramaEducativo() {
        ProgramaEducativoDAO programaEducativoDAO = new ProgramaEducativoDAO();
        ArrayList<ProgramaEducativo> programasEducativos = programaEducativoDAO.findProgramasEducativosByName("");

        for(ProgramaEducativo programaEducativo : programasEducativos) {
            cbProgramaEducativo.addItem(programaEducativo);
        }
    }

    private void llenarCBTutorAcademico() {
        TutorAcademicoDAO tutorAcademicoDAO = new TutorAcademicoDAO();
        ArrayList<TutorAcademico> tutoresAcademicos = tutorAcademicoDAO.findTutoresAcademicosByName("");

        for(TutorAcademico tutorAcademico : tutoresAcademicos) {
            cbTutorAcademico.addItem(tutorAcademico);
        }
    }

